    private void RegistrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_RegistrarMouseClicked
    List<String> datos = new ArrayList<>();
    int aux=0;
                                //Captura la informaci칩n de la venta
    datos.add(Nom_cliente.getText());
    datos.add(String.valueOf(Ciudad.getSelectedItem()));
    datos.add(String.valueOf(Edad.getSelectedItem()));
    datos.add(Precio.getText());
    datos.add(String.valueOf(Calendario.getSelectedDate().get(1)));   //a침o
    datos.add(String.valueOf(Calendario.getSelectedDate().get(2)+1));   //mes, enero lo toma como mes 
    datos.add(String.valueOf(Calendario.getSelectedDate().get(5)));   //dia del mes    
    Venta venta = new Venta(datos,id,name,pass);   
    aux=venta.registrar();
    if(aux==200)
        {
        JOptionPane.showMessageDialog(null, "Registro exitoso");
        }
    else JOptionPane.showMessageDialog(null, "Registro fallido\nError HTTP: "+aux);
    Inventario inventario = new Inventario("Actualizar",name,pass);
    aux=inventario.actualizar(id);
        if(aux==200)
        {
        JOptionPane.showMessageDialog(null, "Actualizaci칩n exitosa");
        }
    else JOptionPane.showMessageDialog(null, "Actualizaci칩n fallida\nError HTTP: "+aux);
    }//GEN-LAST:event_RegistrarMouseClicked

